package fi.eriran._2020.day10.pojo;

public class JoltDifferencesContainer {

    private int oneJolts;
    private int twoJolts;
    private int threeJolts;

    public JoltDifferencesContainer() {
        oneJolts = 0;
        twoJolts = 0;
        threeJolts = 0;
    }

    public int getOneJolts() {
        return oneJolts;
    }

    public void setOneJolts(int oneJolts) {
        this.oneJolts = oneJolts;
    }

    public int getTwoJolts() {
        return twoJolts;
    }

    public void setTwoJolts(int twoJolts) {
        this.twoJolts = twoJolts;
    }

    public int getThreeJolts() {
        return threeJolts;
    }

    public void setThreeJolts(int threeJolts) {
        this.threeJolts = threeJolts;
    }
}
